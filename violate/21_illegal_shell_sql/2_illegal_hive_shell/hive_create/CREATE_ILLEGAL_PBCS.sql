CREATE EXTERNAL TABLE IF NOT EXISTS ILLEGAL_PBCS_PASSNUM(
  TX_DT STRING,
  BRANCH_IDASQUERY_ORG STRING,
  DEBIT_ACCT_NUM STRING,
  CARD_NO STRING,
  CUSTOMER_NAME STRING,
  CR_OPUN_COD STRING,
