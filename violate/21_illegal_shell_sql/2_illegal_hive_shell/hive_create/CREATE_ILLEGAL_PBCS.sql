  TX_DTASQUE_DATE STRING,
  TX_OPERATOR STRING,
  ID_CRDT_NOASCRDT_NO STRING,
  BRANCH_ID STRING,

  ID_CRDT_NO STRING,
  CARD_NO STRING
)
PARTITIONED BY (P9_DATA_DATE string)
STORED AS ORC;


